#ifdef OLED_ENABLE

oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    return OLED_ROTATION_270;  // flips the display 270 degrees
}

bool oled_task_user(void) {


    led_usb_state = host_keyboard_led_state();

    // Host Keyboard Layer Status
    oled_set_cursor(0, 0);
    switch (get_highest_layer(layer_state)) {
    case _BL:
        oled_write_ln_P(PSTR("BASE"), false);
        break;
    case _NM:
        oled_write_ln_P(PSTR("NUM"), false);
        break;
    case _FN:
        oled_write_ln_P(PSTR("RGB"), false);
        break;
    case _GM:
        oled_write_ln_P(PSTR("GAME"), false);
        break;
    default:
        oled_write_ln_P(PSTR("UND"), false);
    }

    oled_set_cursor(0, 1);
    oled_write_P(PSTR("-----"), false);
    oled_write_P(led_usb_state.num_lock ? PSTR("num:*") : PSTR("num:."), false);
    oled_write_P(led_usb_state.caps_lock ? PSTR("cap:*") : PSTR("cap:."), false);
    oled_write_P(led_usb_state.scroll_lock ? PSTR("scr:*") : PSTR("scr:."), false);

    oled_write_P(PSTR("-----"), false);
    static char led_buf[30];
    snprintf(led_buf, sizeof(led_buf) - 1, "RGB:%cM: %2dh: %2ds: %2dv: %2d\n",
        rgblight_is_enabled() ? '*' : '.', (uint8_t)rgblight_get_mode(),
        (uint8_t)(rgblight_get_hue() / RGBLIGHT_HUE_STEP),
        (uint8_t)(rgblight_get_sat() / RGBLIGHT_SAT_STEP),
        (uint8_t)(rgblight_get_val() / RGBLIGHT_VAL_STEP));
    oled_write(led_buf, false);


    render_luna(0, 13);

    return false;
}
#endif


bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {

            /* KEYBOARD PET STATUS START */

        case KC_VOLU:
            if (record->event.pressed) {
                isUp = true;
            } else {
                isUp = false;
            }
            break;
        case KC_VOLD:
            if (record->event.pressed) {
                isDown = true;
            } else {
                isDown = false;
            }
            break;
        case KC_MPLY:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;

            /* KEYBOARD PET STATUS END */
    }
    return true;
}
